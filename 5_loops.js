// loops execute number of code a number of times
// The For Loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}
// The While Loop
let i = 0;
while (i < 10) {
    console.log(i);
    i++;
}

// The Do While Loop - executes the code block, before checking if the condition is true
let a = 0;
do {
    console.log("Hello Javascript");
    a++
}
while( a < 5);
